/**
 * Категория материала
 * Определяет источник и способ получения
 */
export type MaterialCategory =
  | 'metal'      // Металлы (руды из недр)
  | 'organic'    // Органика (кожа, шёлк, кости)
  | 'mineral'    // Минералы (камень, нефрит, кристаллы)
  | 'wood'       // Дерево (из лесов и мест силы)
  | 'crystal';   // Кристаллы Ци (духовные камни)

/**
 * Описание категорий
 * 
 * metal: Добываются из недр мира (0-6000 км глубина)
 *        Источник: лей-линии, магматические карманы
 *        Лор: "Недра не бесконечны — они ограничены глубиной в 6 000 км"
 * 
 * organic: Получаются от монстров, духовных зверей, древних червей
 *          Лор: "Кости Ци монстров, 200 ед/см³"
 *          Лор: "Духовный шёлк" — от духовных червей
 * 
 * mineral: Камни, мрамор, нефрит из мест силы
 *          Лор: "Места силы — с радиусом от 100 до 1 000 км"
 *          Лор: "Нефрит. Идеален для культивации" (высокая проводимость Ци)
 * 
 * wood: Деревья из лесов и мест силы
 *        Лор: "Дерево из пустоты между мирами" (особый тип)
 * 
 * crystal: Кристаллы Ци (духовные камни)
 *          Лор: "кристаллы Ци (камни духа) плотность 1024 ед/см³"
 *          Лор: "Кристаллизованная Ци"
 */

// ============================================================================
// PROPERTIES
// ============================================================================

/**
 * Физические свойства материала
 * Влияют на параметры экипировки
 */
export interface MaterialProperties {
  /**
   * Базовая прочность (10-150)
   * Определяет maxDurability экипировки
   * 
   * Правила:
   * - Твёрдые металлы и минералы: высокое значение
   * - Органика и ткани: низкое значение
   * - Тир влияет: T1=10-40, T5=80-150
   */
  durability: number;

  /**
   * Проводимость Ци (0-100+)
   * % эффективности передачи Ци через материал
   * 
   * Обоснование из лора:
   * - "Ци может насыщать твердые объекты"
   * - "для неорганических: 100 ед/см³"
   * - "для органических (кости монстров): 200 ед/см³"
   * 
   * Правила:
   * - Обычные металлы: 5-20%
   * - Духовные металлы: 30-60%
   * - Небесные материалы: 70-100%
   * - Кристаллы Ци: 100%+
   */
  qiConductivity: number;

  /**
   * Вес (0.01-10.0)
   * Влияет на скорость передвижения
   * 
   * Правила:
   * - Тяжёлые металлы: 3-5
   * - Лёгкие ткани: 0.1-0.5
   * - Пустотные материалы: очень лёгкие
   */
  weight: number;

  /**
   * Твёрдость (1-10)
   * Влияет на пробитие брони и урон оружия
   * 
   * Правила:
   * - Мягкие материалы: 1-3
   * - Средние: 4-6
   * - Твёрдые: 7-10
   */
  hardness: number;

  /**
   * Гибкость (1-10)
   * Влияет на уклонение и парирование
   * 
   * Правила:
   * - Жёсткие: 1-3
   * - Средние: 4-6
   * - Гибкие: 7-10
   */
  flexibility: number;
}

// ============================================================================
// BONUSES
// ============================================================================

/**
 * Бонус материала
 * Добавляется к параметрам экипировки
 */
export interface MaterialBonus {
  /**
   * ID бонуса из BonusRegistry
   * Примеры: combat_damage, defense_armor, qi_regeneration
   */
  type: string;

  /**
   * Значение бонуса
   * Для фиксированных: абсолютное значение
   * Для множителей: в процентах
   */
  value: number;

  /**
   * Является ли множителем
   * true: value — процент (например, +15% урона)
   * false: value — фиксированное значение (например, +10 урона)
   */
  isMultiplier: boolean;
}

// ============================================================================
// MAIN DEFINITION
// ============================================================================

/**
 * Полное определение материала
 */
export interface MaterialDefinition {
  /**
   * Уникальный ID материала
   * Формат: snake_case
   * Примеры: iron, spirit_iron, star_metal, dragon_bone
   */
  id: string;

  /**
   * Отображаемое название
   * Примеры: "Железо", "Духовное железо", "Звёздный металл"
   */
  name: string;

  /**
   * Тир материала (1-5)
   */
  tier: MaterialTier;

  /**
   * Категория материала
   */
  category: MaterialCategory;

  /**
   * Физические свойства
   */
  properties: MaterialProperties;

  /**
   * Бонусы, которые материал даёт экипировке
   */
  bonuses: MaterialBonus[];

  /**
   * Описание материала (для UI)
   */
  description: string;

  /**
   * Редкость (шанс выпадения, 0.1-100)
   * Выше = чаще выпадает
   * 
   * Правила:
   * - T1: 50-100 (частые)
   * - T2: 20-60
   * - T3: 5-20
   * - T4: 1-5
   * - T5: 0.1-1 (редчайшие)
   */
  rarity: number;

  /**
   * Источник материала (из лора)
   */
  source?: string;

  /**
   * Требования к уровню культивации для обработки
   */
  requiredLevel?: number;
}

// ============================================================================
// SPECIAL TYPES
// ============================================================================

/**
 * Специальный тип материала
 * Для уникальных материалов с особыми свойствами
 */
export type SpecialMaterialType =
  | 'normal'           // Обычный материал
  | 'spirit'           // Духовный (насыщен Ци)
  | 'heavenly'         // Небесный (из высших сфер)
  | 'void'             // Пустотный (из межмирья)
  | 'chaos'            // Хаотичный (содержит хаотичную Ци)
  | 'primordial';      // Первородный (из начала времён)

/**
 * Расширенное определение материала
 * Для специальных материалов
 */
export interface SpecialMaterialDefinition extends MaterialDefinition {
  specialType: SpecialMaterialType;
  
  /**
   * Особые эффекты материала
   */
  specialEffects: string[];
  
  /**
   * Совместимость с типами экипировки
   */
  compatibleWith: ('weapon' | 'armor' | 'charger' | 'accessory')[];
}

// ============================================================================
// UTILITY TYPES
