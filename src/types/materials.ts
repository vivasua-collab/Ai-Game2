/**
 * Результат выбора материала
 */
export interface MaterialSelectionResult {
  materialId: string;
  material: MaterialDefinition;
  reason: 'specified' | 'level_match' | 'region_match' | 'random';
}

/**
 * Конфигурация генерации материала
 */
export interface MaterialGenerationConfig {
  minTier?: MaterialTier;
  maxTier?: MaterialTier;
  category?: MaterialCategory;
  excludeIds?: string[];
  preferHighRarity?: boolean;
}
